    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:

        if (!txtidactividad.getText().equals("")) {
            int confirmation = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar la actividad");

            if (confirmation==0) {
                factividad func= new factividad();
                vactividad dts=new vactividad();

                dts.setIdactividad(Integer.parseInt(txtidactividad.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
           }

        }
    }//GEN-LAST:event_btneliminarActionPerformed

