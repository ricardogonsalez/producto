    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txttipo.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un tipo");
            txttipo.requestFocus();
            return;
        }
        if (txttitulo.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un titulo");
            txttitulo.requestFocus();
            return;
        }
        if (txtlugar.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un lugar");
            txtlugar.requestFocus();
            return;
        }
        if (txtduracion.getText().length() == 0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una duracion");
            txtduracion.requestFocus();
            return;
       
        }

        vactividad dts = new vactividad();
        factividad func = new factividad();

        dts.setTipo(txttipo.getText());
        dts.setTitulo(txttitulo.getText());
        dts.setLugar(txtlugar.getText());
        dts.setDuracion(txtduracion.getText());
 
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "La actividad fue regisrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }

        }

        else if (accion.equals("editar")) {
            dts.setIdactividad(Integer.parseInt(txtidactividad.getText()));
            if (func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "La actividad fue editado satisfactoriamente");
                mostrar("");
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btnguardarActionPerformed

